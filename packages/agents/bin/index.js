#!/usr/bin/env node

/**
 * @kirill.konshin/agents
 *
 * This script symlinks all markdown rule files from the package into the consuming
 * project's `.claude/rules` directory (auto-discovered by Claude Code) and generates
 * an `AGENTS.md` file for other tools, with `CLAUDE.md` symlinked to `AGENTS.md`.
 *
 * Usage:
 *   npx agents init    - Initialize AGENTS.md in the current directory
 *   npx agents update  - Force update AGENTS.md even if it exists
 *   npx agents         - Same as init (default)
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const AGENTS_FILE = 'AGENTS.md';
const CLAUDE_FILE = 'CLAUDE.md';
const CLAUDE_RULES_DIR = '.claude/rules';
const RULES_DIR = path.join(__dirname, '..', 'rules');
const HEADER = `# AI Agent Rules

This file is auto-generated by @kirill.konshin/agents.
Do not edit directly - customize by adding your own rules below the generated section.

---

`;

const FOOTER = `
---

# Custom Rules

Add your project-specific rules below this line.
`;

const GENERATED_MARKER = '<!-- @kirill.konshin/agents:generated -->';

/**
 * Read all markdown files from the package rules directory.
 */
function readRules() {
    const rules = [];

    if (!fs.existsSync(RULES_DIR)) {
        console.error(`Rules directory not found: ${RULES_DIR}`);
        process.exit(1);
    }

    const files = fs
        .readdirSync(RULES_DIR)
        .filter((f) => f.endsWith('.md'))
        .sort();

    for (const file of files) {
        const filePath = path.join(RULES_DIR, file);
        const content = fs.readFileSync(filePath, 'utf-8');
        rules.push({
            file,
            name: file.replace('.md', ''),
            content,
        });
    }

    return rules;
}

/**
 * Fail if `.claude/rules` contains anything that isn't a symlink - we don't want to
 * silently blow away a real file/directory a user placed there by hand.
 */
function checkRulesDirSafety(rulesTargetDir) {
    if (!fs.existsSync(rulesTargetDir)) return;

    for (const entry of fs.readdirSync(rulesTargetDir)) {
        const entryPath = path.join(rulesTargetDir, entry);

        if (!fs.lstatSync(entryPath).isSymbolicLink()) {
            console.error(`${CLAUDE_RULES_DIR}/${entry} exists and is not a symlink.`);
            console.error('Remove or back it up manually, then re-run.');
            process.exit(1);
        }
    }
}

/**
 * Fail if `CLAUDE.md` exists and isn't a symlink - same reasoning as checkRulesDirSafety.
 */
function checkClaudeMdSafety(claudeMdPath) {
    let stat;

    try {
        stat = fs.lstatSync(claudeMdPath);
    } catch {
        return;
    }

    if (!stat.isSymbolicLink()) {
        console.error(`${CLAUDE_FILE} exists and is not a symlink.`);
        console.error('Remove or back it up manually, then re-run.');
        process.exit(1);
    }
}

/**
 * Symlink rules into the consuming project's `.claude/rules` directory.
 * Safe to wipe and recreate: checkRulesDirSafety already guaranteed every existing
 * entry is a symlink.
 *
 * FIXME Codex Includes
 *   https://github.com/openai/codex/issues/17401
 *   https://github.com/openai/codex/issues/6038
 *   32kb limit https://github.com/openai/codex/issues/13386
 *
 * TODO: generate `.claude/CLAUDE.md` with an index of the symlinked rules, for humans browsing `.claude/rules` manually
 *   (Claude Code itself doesn't need it - it auto-loads every file under `.claude/rules` regardless of whether it's
 *   indexed anywhere).
 */
function symlinkRules(cwd, rules) {
    const targetDir = path.join(cwd, CLAUDE_RULES_DIR);

    fs.rmSync(targetDir, { recursive: true, force: true });
    fs.mkdirSync(targetDir, { recursive: true });

    for (const rule of rules) {
        const source = path.join(RULES_DIR, rule.file);
        const linkPath = path.join(targetDir, rule.file);
        fs.symlinkSync(path.relative(targetDir, source), linkPath);
    }

    return targetDir;
}

/**
 * Symlink CLAUDE.md -> AGENTS.md so both point at the same generated content.
 */
function symlinkClaudeMd(cwd) {
    const claudeMdPath = path.join(cwd, CLAUDE_FILE);

    fs.rmSync(claudeMdPath, { force: true });
    fs.symlinkSync(AGENTS_FILE, claudeMdPath);
}

/**
 * Generate AGENTS.md with references to the rule files.
 *
 * `.claude/rules/*.md` are symlinked by symlinkRules() above and auto-discovered by
 * Claude Code at launch (confirmed: Claude Code recursively loads every `.md` file
 * under `.claude/rules`, symlinks included) - no reference to them is needed here.
 * The sections below are for other tools that only read AGENTS.md.
 */
function generateAgentsFile(rules, cwd) {
    const relativeRulesDir = path.relative(cwd, RULES_DIR).split(path.sep).join('/');

    const includes = rules.map((rule) => `@include ${relativeRulesDir}/${rule.file}`).join('\n');

    const links = rules.map((rule) => `- [${rule.file}](${relativeRulesDir}/${rule.file})`).join('\n');

    return `${GENERATED_MARKER}\n${HEADER}Rules from @kirill.konshin/agents.

<!-- \`.claude/rules/*.md\` are symlinked automatically on init/update and are
     auto-discovered by Claude Code at launch - no reference needed here. -->

## Codex

<!-- TODO: \`@include\` is not yet supported by Codex (see https://github.com/openai/codex/issues/17401),
     so these lines are inert until that ships. The plain links below work today. -->

${includes}

## Rule files

${links}${FOOTER}`;
}

/**
 * Check if AGENTS.md exists and was generated by this tool
 */
function checkExisting(targetPath) {
    if (!fs.existsSync(targetPath)) {
        return { exists: false, generated: false };
    }

    const content = fs.readFileSync(targetPath, 'utf-8');
    const generated = content.includes(GENERATED_MARKER);

    return { exists: true, generated };
}

/**
 * Main function
 */
function main() {
    const args = process.argv.slice(2);
    const command = args[0] || 'init';
    const cwd = process.env.INIT_CWD || process.cwd();
    const targetPath = path.join(cwd, AGENTS_FILE);
    const claudeMdPath = path.join(cwd, CLAUDE_FILE);
    const rulesTargetDir = path.join(cwd, CLAUDE_RULES_DIR);

    console.log(`@kirill.konshin/agents: ${command}`);
    console.log(`Target: ${targetPath}`);

    const { exists, generated } = checkExisting(targetPath);

    if (command === 'init') {
        if (exists && !generated) {
            console.log(`${AGENTS_FILE} exists and was not generated by this tool. Skipping.`);
            console.log('Use "npx agents update" to force overwrite, then merge manually.');
            return;
        }

        if (exists && generated) {
            console.log(`${AGENTS_FILE} exists and was generated by this tool. Updating...`);
        }
    }

    if (command === 'update' || command === 'init') {
        checkRulesDirSafety(rulesTargetDir);
        checkClaudeMdSafety(claudeMdPath);

        const rules = readRules();
        console.log(`Found ${rules.length} rule files: ${rules.map((r) => r.name).join(', ')}`);

        const targetRulesDir = symlinkRules(cwd, rules);
        console.log(`Symlinked rules to ${targetRulesDir}`);

        const content = generateAgentsFile(rules, cwd);
        fs.writeFileSync(targetPath, content, 'utf-8');
        console.log(`${exists ? 'Updated' : 'Created'} ${AGENTS_FILE}`);

        symlinkClaudeMd(cwd);
        console.log(`Symlinked ${CLAUDE_FILE} -> ${AGENTS_FILE}`);
        return;
    }

    console.log(`Unknown command: ${command}`);
    console.log('Usage: npx agents [init|update]');
    process.exit(1);
}

main();
